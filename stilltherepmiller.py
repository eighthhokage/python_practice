#using the tab paramater and title method

name = "phillip"
last_name = "miller"

print(name.title() + " " + last_name.title())
print("\tHello," + name + " " + last_name)   # the \t is tab so each instance below the line is a further indent/tab 
print("\t\tHello," + name + " " + last_name)  
print("\t\t\tHello," + name + " " + last_name)  
print("\t\t\t\tHello," + name + " " + last_name)

print("Are " + "\nYou\nStill\nThere ?") 
